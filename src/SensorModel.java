public class SensorModel {
	private int nodeId;
	private int température;
	private int luminosité;
	private int sequence;

	public int getNodeId() {
		return nodeId;
	}

	public void setNodeId(int nodeId) {
		this.nodeId = nodeId;
	}

	public int getTempérature() {
		return température;
	}

	public void setTempérature(int température) {
		this.température = température;
	}

	public int getLuminosité() {
		return luminosité;
	}

	public void setLuminosité(int luminosité) {
		this.luminosité = luminosité;
	}

	public int getSequence() {
		return sequence;
	}

	public void setSequence(int sequence) {
		this.sequence = sequence;
	}

}
